import { Category } from "@/sanityTemp/Types/Category"; // Importing Category type from the specified path
interface CategoryObject {
    [key: string]: any;
}

function processCategories(categories: string[]) {
    const categoryObject: CategoryObject = {};

    const formatString = (str: string) => str.replace(/[,&]/g, '').replace(/\s+/g, '');
    const toCamelCase = (str: string) => str.charAt(0).toLowerCase() + str.slice(1);
    const capitalizeFirstLetter = (str: string) => str.charAt(0).toUpperCase() + str.slice(1);

    categories.forEach(categoryStr => {
        const parts = categoryStr.split("/");
        let currentPath = "";
        const parents: string[] = []; // To store the parents of the current category

        for (let i = 0; i < parts.length; i++) {
            const category = parts[i];
            currentPath += (i === 0 ? "" : "-") + category; // Change separator to hyphen
            parents.push(category); // Add category as a parent

            if (!categoryObject[currentPath]) {
                let nickNameParts = parts.slice(0, i + 1).map((p, index) => {
                    const formattedPart = formatString(p);
                    return index === 0 ? formattedPart.toLowerCase() : toCamelCase(formattedPart);
                });
                nickNameParts[nickNameParts.length - 1] = capitalizeFirstLetter(nickNameParts[nickNameParts.length - 1]);
                const nickName = nickNameParts.join('_');

                let breadCrumbs = parts.slice(0, i).map(p => p.replace(/&/g, 'and'));

                categoryObject[currentPath] = {
                    category: category,
                    parentCategory: i > 0 ? parts[i - 1] : undefined,
                    childrenCategories: [],
                    relatedCategories: [],
                    order: i + 1,
                    nickName: nickName,
                    mainCategory: parts[0],
                    breadCrumbs: breadCrumbs,
                    seoTitle: [{
                        _type: 'block',
                        _key: 'seoTitle1',
                        children: [
                            {
                                _type: 'span',
                                _key: 'seoTitleSpan1',
                                text: category === 'For Sale' ? `Find what you're looking for on Oly: Your Trusted Classifieds Hub` : `Explore ${category} Listings on Oly â€” Your Trusted Classifieds Hub.`,
                            }
                        ],
                        markDefs: []
                    }],
                    seoDescription: [{
                        _type: 'block',
                        _key: 'seoDescription1',
                        children: [
                            {
                                _type: 'span',
                                _key: 'seoDescriptionSpan1',
                                text: category === 'For Sale' ? `Experience convenience and discover a wide range listings on Oly. Oly provides a user-friendly platform to explore a wide range of listings. Find exactly what you need and make your next purchase or sale hassle-free.` : `Experience convenience and variety with Oly's ${category} listings. Oly provides a user-friendly platform to explore a wide range of categories. Find exactly what you need in the ${category} category and make your next purchase or sale hassle-free.`,
                            }
                        ],
                        markDefs: []
                    }],
                    altCategoryName: currentPath, // Use currentPath directly
                    parents: [...parents], // Add parents array
                };
            }

            if (i < parts.length - 1) {
                const childCategory = parts[i + 1];
                if (!categoryObject[currentPath]?.childrenCategories.includes(childCategory)) {
                    categoryObject[currentPath]?.childrenCategories.push(childCategory);
                }
            }

            if (categoryObject[currentPath].parentCategory) {
                categoryObject[currentPath].relatedCategories.push(categoryObject[currentPath].parentCategory);
            }

            categoryObject[currentPath].relatedCategories.push(...categoryObject[currentPath].childrenCategories);
        }
    });

    return Object.values(categoryObject);
}

